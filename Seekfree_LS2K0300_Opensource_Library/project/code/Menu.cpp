// 菜单选项 标志位
uint8_t menu_flag = 1;

void Menu_Show(void)
{
    Menu_UI(1);
    ips200_show_string(0  ,32 , ">");

    while(1)
    {
        // 存储确认键被按下时menuflag的值的临时变量，默认为无效值0
		uint8_t menu_flag_temp = 0;
		// 上/下按键是否被按下过
		uint8_t key_pressed = 0;

        /* 按键处理*/
        if (gpio_get_level(KEY_UP) == 0)
        {
            key_pressed = 1;
            menu_flag --;
            if (menu_flag < 1)menu_flag = 1;
        }
        else if (gpio_get_level(KEY_DOWN) == 0)
        {
            key_pressed = 1;
            menu_flag ++;
            if (menu_flag > 1)menu_flag = 1;
        }
        else if (gpio_get_level(KEY_CONFIRM) == 0)
        {
            menu_flag_temp = menu_flag;
        }
//       else if (gpio_get_level(KEY_BACK) == 0)
//        {
//
//        }


        /* 模式跳转*/
        // Debug模式界面
        if (menu_flag_temp == 1)
        {
            ips200_clear();
            Debug_Page_Menu();

            // 从Debug界面返回，显示主菜单界面
            ips200_clear();
            Menu_UI(1);
            ips200_show_string(0  ,32 , ">");
        }


        /* 显示更新*/
        if (key_pressed)
        {
            switch (menu_flag)
            {
                // 在"Debug模式"的光标
                case 1:
                    ips200_show_string(0  ,32 , ">");
                    break;
            }
        }
    }
}
