// [一级界面]主菜单界面
void Menu_UI(uint8_t Page)
{
	switch(Page)
	{		
		// 第一页
		case 1:
		{
            ips200_show_string(8  ,0  , "Menu");
            ips200_show_string(0  ,16 , "==============================");
            ips200_show_string(10 ,32 , "Debug");

			break;
		}
	}
}
